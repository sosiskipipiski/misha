package sololessons;

public class les1 { // Вывод. База

    public static void main(String[] args) {
        System.out.println("Hello world!"); // println переносит на другие строки

        System.out.print("Hello world!"); // print выводит все на одной строке

        System.out.println("Hello \n\n world!"); // \n переносит на другую строку

        System.out.println("\t Hello \t world"); // \t выводит с табом-отступом

        System.out.println("Hello world \" \\"); // \\ выводит слеш и \" что бы вывести ковычки без ошибок
    }

}
