package sololessons;

public class les5 {
    public static void main(String[] args) { // Цикл for
        for(float i = 100; i > 10; i /= 2) {
            // 1 - с какого числа начнется цикл
            // 2 - при каких обстоят. будет продолжаться цикл
            // 3 - что делать циклу
            System.out.println(i);
        }
    }
}
