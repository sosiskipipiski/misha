package sololessons;

public class les5s2 {
    public static void main(String[] args) { // Цикл while
        // Отличается от цикла for лишь форматом записи
        int i = 0; // В отличии от цикла for нужно создавать перменную отдельно

        while(i < 10) { // Указываем условие
            System.out.println(i); // Выводим переменную на экран
            i++; // Действие цикла
        }
    }
}
